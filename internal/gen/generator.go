// Package gen implements the sqlc-gen-bulk-insert code generation logic.
// It is invoked via codegen.Run(Generate) in main.go.
package gen

import (
	"bytes"
	"context"
	"fmt"
	"go/format"
	"strconv"
	"strings"
	"text/template"

	"github.com/sqlc-dev/plugin-sdk-go/plugin"
)

// ─── Data types used by the template ─────────────────────────────────────────

// BulkFunc describes a single bulk-insert function to be generated.
type BulkFunc struct {
	// FuncName is the generated function name, e.g. "BulkInsertUser".
	FuncName string
	// ConstName is the package-level constant holding the SQL prefix,
	// e.g. "bulkInsertUserPrefix".
	ConstName string
	// ParamsType is the element type of the args slice, e.g. "InsertUserParams"
	// (for 2+ params) or "int64" (for exactly 1 param).
	ParamsType string
	// InsertPrefix is a Go string literal (produced by strconv.Quote) for the
	// SQL text up to and including "VALUES ", ready to be emitted verbatim into
	// a generated const declaration. Examples:
	//   `"INSERT INTO users (name, email) VALUES "`
	//   `"INSERT INTO \`users\` (\`name\`, \`email\`) VALUES "`
	InsertPrefix string
	// Placeholder is the per-row placeholder string, e.g. "(?, ?)".
	Placeholder string
	// NumParams is the number of ? per row.
	NumParams int
	// ValueArgsLine is the pre-built argument list for the append call, e.g.:
	//   "arg.Name, arg.Email"  (multi-param)
	//   "arg"                   (single-param)
	ValueArgsLine string
	// SourceFile is the SQL filename this query originated from, e.g. "users.sql".
	// Used by the "file" split mode to group functions into per-source files.
	SourceFile string
	// NeedsTime / NeedsJSON / NeedsSQL indicate extra imports required by
	// the types used in this function.
	NeedsTime bool
	NeedsJSON bool
	NeedsSQL  bool
}

// fileData is the top-level context passed to the Go template.
type fileData struct {
	Package   string
	Funcs     []BulkFunc
	NeedsTime bool
	NeedsJSON bool
	NeedsSQL  bool
}

// ─── Template ─────────────────────────────────────────────────────────────────

// bulkInsertTmpl is the text/template that generates the output .go file.
//
// InsertPrefix is stored as a strconv.Quote-produced Go string literal
// (double-quoted, with all special characters properly escaped). This safely
// handles SQL that contains backtick-quoted identifiers, which are common in
// MySQL/MariaDB, without breaking the generated raw-string literal.
const bulkInsertTmpl = `// Code generated by sqlc-gen-bulk-insert. DO NOT EDIT.

package {{.Package}}

import (
	"context"
	"strings"
{{- if .NeedsTime}}
	"time"
{{- end}}
{{- if .NeedsJSON}}
	"encoding/json"
{{- end}}
{{- if .NeedsSQL}}
	"database/sql"
{{- end}}
)
{{range .Funcs}}
const {{.ConstName}} = {{.InsertPrefix}}

// {{.FuncName}} performs a bulk INSERT of multiple rows in a single statement.
// Passing an empty slice is a no-op.
func (q *Queries) {{.FuncName}}(ctx context.Context, args []{{.ParamsType}}) error {
	if len(args) == 0 {
		return nil
	}
	valueStrings := make([]string, 0, len(args))
	valueArgs := make([]any, 0, len(args)*{{.NumParams}})
	for _, arg := range args {
		valueStrings = append(valueStrings, "{{.Placeholder}}")
		valueArgs = append(valueArgs, {{.ValueArgsLine}})
	}
	query := {{.ConstName}} + strings.Join(valueStrings, ", ")
	_, err := q.db.ExecContext(ctx, query, valueArgs...)
	return err
}
{{end}}`

// bulkInsertTemplate is the parsed form of bulkInsertTmpl, initialized once
// at package load time. Parsing once avoids repeated overhead when
// renderFile is called for each output file, and surfaces any template
// syntax errors immediately at startup rather than during generation.
var bulkInsertTemplate = template.Must(template.New("bulk").Parse(bulkInsertTmpl))

// ─── Entry point ──────────────────────────────────────────────────────────────

// Generate is the codegen.Handler called by codegen.Run.
// It filters the incoming queries, builds BulkFunc descriptors, then delegates
// to the appropriate file-rendering strategy based on opts.SplitBy.
func Generate(_ context.Context, req *plugin.GenerateRequest) (*plugin.GenerateResponse, error) {
	opts, err := parseOptions(req.PluginOptions)
	if err != nil {
		return nil, err
	}

	var funcs []BulkFunc
	for _, q := range req.Queries {
		if !isInsertQuery(q) {
			continue
		}
		if len(q.Params) == 0 {
			// Nothing to bulk-insert if there are no parameters.
			continue
		}
		bf, err := buildBulkFunc(q)
		if err != nil {
			return nil, fmt.Errorf("sqlc-gen-bulk-insert: query %q: %w", q.Name, err)
		}
		funcs = append(funcs, bf)
	}

	if len(funcs) == 0 {
		return &plugin.GenerateResponse{}, nil
	}

	files, err := renderFiles(opts, funcs)
	if err != nil {
		return nil, err
	}
	return &plugin.GenerateResponse{Files: files}, nil
}

// ─── File rendering ───────────────────────────────────────────────────────────

// renderFiles dispatches to the correct splitting strategy.
func renderFiles(opts *Options, funcs []BulkFunc) ([]*plugin.File, error) {
	switch opts.SplitBy {
	case "file":
		return renderBySourceFile(opts.Package, funcs)
	case "query":
		return renderByQuery(opts.Package, funcs)
	default: // "single"
		contents, err := renderFile(opts.Package, funcs)
		if err != nil {
			return nil, err
		}
		return []*plugin.File{{Name: opts.OutFilename, Contents: contents}}, nil
	}
}

// renderBySourceFile groups BulkFuncs by the SQL file they originated from and
// produces one output file per group.
//
//	users.sql    → bulk_users.go
//	products.sql → bulk_products.go
func renderBySourceFile(pkg string, funcs []BulkFunc) ([]*plugin.File, error) {
	// Preserve insertion order of source files seen.
	order := make([]string, 0)
	groups := make(map[string][]BulkFunc)
	for _, f := range funcs {
		if _, seen := groups[f.SourceFile]; !seen {
			order = append(order, f.SourceFile)
		}
		groups[f.SourceFile] = append(groups[f.SourceFile], f)
	}

	files := make([]*plugin.File, 0, len(groups))
	for _, src := range order {
		contents, err := renderFile(pkg, groups[src])
		if err != nil {
			return nil, err
		}
		files = append(files, &plugin.File{
			Name:     sourceFileToOutName(src),
			Contents: contents,
		})
	}
	return files, nil
}

// renderByQuery produces one output file per BulkFunc.
//
//	BulkInsertUser    → bulk_insert_user.go
//	BulkInsertProduct → bulk_insert_product.go
func renderByQuery(pkg string, funcs []BulkFunc) ([]*plugin.File, error) {
	files := make([]*plugin.File, 0, len(funcs))
	for _, f := range funcs {
		contents, err := renderFile(pkg, []BulkFunc{f})
		if err != nil {
			return nil, err
		}
		files = append(files, &plugin.File{
			Name:     queryFuncToOutName(f.FuncName),
			Contents: contents,
		})
	}
	return files, nil
}

// renderFile renders the Go template for a slice of BulkFuncs and returns
// gofmt-formatted source bytes.
func renderFile(pkg string, funcs []BulkFunc) ([]byte, error) {
	data := fileData{Package: pkg, Funcs: funcs}
	for _, f := range funcs {
		if f.NeedsTime {
			data.NeedsTime = true
		}
		if f.NeedsJSON {
			data.NeedsJSON = true
		}
		if f.NeedsSQL {
			data.NeedsSQL = true
		}
	}

	var buf bytes.Buffer
	if err := bulkInsertTemplate.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("sqlc-gen-bulk-insert: executing template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf(
			"sqlc-gen-bulk-insert: formatting generated source: %w\n---\n%s",
			err, buf.String(),
		)
	}
	return formatted, nil
}

// ─── Query filtering ──────────────────────────────────────────────────────────

// isInsertQuery returns true when q is an INSERT statement with a command type
// that produces no output rows (:exec, :execresult, :execrows).
func isInsertQuery(q *plugin.Query) bool {
	if q.InsertIntoTable == nil {
		return false
	}
	switch q.Cmd {
	case ":exec", ":execresult", ":execrows":
		return true
	}
	return false
}

// ─── BulkFunc builder ─────────────────────────────────────────────────────────

// buildBulkFunc builds the template data for a single bulk-insert function.
func buildBulkFunc(q *plugin.Query) (BulkFunc, error) {
	prefix, ok := extractInsertPrefix(q.Text)
	if !ok {
		return BulkFunc{}, fmt.Errorf(
			"cannot find VALUES keyword in query text: %q", q.Text,
		)
	}

	n := len(q.Params)
	placeholder := buildPlaceholder(n)
	funcName := "Bulk" + q.Name
	constName := lowerFirst(funcName) + "Prefix"

	var (
		paramsType    string
		valueArgExprs []string
		needsTime     bool
		needsJSON     bool
		needsSQL      bool
	)

	if n == 1 {
		// Single-parameter query: the slice element IS the Go type directly.
		col := q.Params[0].Column
		typeName, hint := goType(col)
		paramsType = typeName
		valueArgExprs = []string{"arg"}
		switch hint {
		case "time":
			needsTime = true
		case "encoding/json":
			needsJSON = true
		case "database/sql":
			needsSQL = true
		}
	} else {
		// Multi-parameter query: reference the QueryNameParams struct that
		// sqlc's main codegen emits (same output package).
		// The individual column Go types do NOT appear in our generated file
		// (they live inside the params struct), so no extra imports are needed.
		paramsType = q.Name + "Params"
		for _, p := range q.Params {
			fieldName := toPascalCase(p.Column.Name)
			valueArgExprs = append(valueArgExprs, "arg."+fieldName)
		}
	}

	return BulkFunc{
		FuncName:      funcName,
		ConstName:     constName,
		ParamsType:    paramsType,
		InsertPrefix:  strconv.Quote(prefix),
		Placeholder:   placeholder,
		NumParams:     n,
		ValueArgsLine: strings.Join(valueArgExprs, ", "),
		SourceFile:    q.Filename,
		NeedsTime:     needsTime,
		NeedsJSON:     needsJSON,
		NeedsSQL:      needsSQL,
	}, nil
}
